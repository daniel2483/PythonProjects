
print("Finding the max number and min number...")

x=float(input("Enter 1st number: "))
y=float(input("Enter 2nd number: "))
z=float(input("Enter 3rd number: "))
w=float(input("Enter 4th number: "))

print("\nResults:")
print("The max value is: ", max(x,y,z,w))
print("The min value is: ", min(x,y,z,w))
print("The Summatory is: ", str((float(x)+float(y)+float(z)+float(w))))
print("The Average is: ", str((float(x)+float(y)+float(z)+float(w))/4))


